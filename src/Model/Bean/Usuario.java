package Model.Bean;

/**
 *
 * @author Samuel
 */
public class Usuario {
    
    private int id;
    private String login;
    private String senhha;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getLogin() {
        return login;
    }

    public void setLogin(String login) {
        this.login = login;
    }

    public String getSenhha() {
        return senhha;
    }

    public void setSenhha(String senhha) {
        this.senhha = senhha;
    }
    
    
    
}
